package users.utills;

public class Date {
    private String y;
    private String m;
    private String d;
    private String h;
    private String min;
    private String sec;
    public Date(){

    }
    public Date(String y, String m, String d, String h, String min, String sec){
        this.y = y;
        this.m = m;
        this.d = d;
        this.h = h;
        this.min = min;
        this.sec = sec;
    }

}
